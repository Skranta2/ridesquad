export function useTranslation() {
  const { language } = useAppSettings();

  const t = useCallback(
    (key: string) => {
      const dict = dictionaries[language];
      return dict[key] ?? dictionaries.en[key] ?? key;
    },
    [language]
  );

  return { t, language };
}
